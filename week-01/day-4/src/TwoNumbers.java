public class TwoNumbers {
    public static void main(String[] args) {

        int a, b;
        a= 22;
        b= 13;

        System.out.println(a + b);
        System.out.println(a - b);
        System.out.println(a * b);
        System.out.println(a / (double)b);
        System.out.println(a/b);
        System.out.println(a%b);

    }
}
